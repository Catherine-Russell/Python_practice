def greet(name):
    print(f"Hi, {name}!")

def add_2_numbers(x, y):
    return x + y

def add_3_numbers(x, y, z):
    return x + y + z

def concatenate_names(name1, name2, name3):
    print("My friends are: " + name1 + ", " + name2 + " and " + name3)

def seconds_per_week(weeks):
    return weeks * 7 * 24 * 360
